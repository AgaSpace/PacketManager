namespace PacketManager.Core.Abstractions;

/// <summary>
/// Представляет билдер пакета, который может изменять или генерировать 
/// исходящие сетевые пакеты для конкретного игрока.
/// </summary>
public interface IPacketBuilder
{
    /// <summary>
    /// Получает приоритет билдера. Чем выше значение, тем выше приоритет.
    /// При наличии нескольких билдеров для одного типа пакета вызывается 
    /// только билдер с максимальным приоритетом.
    /// </summary>
    int Priority { get; }

    /// <summary>
    /// Получает идентификатор типа пакета (Message ID), который обрабатывает этот билдер.
    /// Например: 16 для PlayerHP, 13 для SyncPlayer и т.д.
    /// </summary>
    int MessageId { get; }

    /// <summary>
    /// Генерирует содержимое пакета в бинарный поток.
    /// </summary>
    /// <param name="context">Контекст сборки, содержащий <see cref="IPacketBuildContext.Writer"/> для записи данных, 
    /// информацию о получателях и оригинальные данные пакета.</param>
    /// <remarks>
    /// Метод должен записывать данные пакета БЕЗ заголовка длины (два первых байта),
    /// так как длина рассчитывается автоматически после выполнения билдера.
    /// Первый байт (<see cref="MessageId"/>) уже записан в поток перед вызовом этого метода.
    /// </remarks>
    void Build(IPacketBuildContext context);
}